// намеренно отдельная от POOL_STATE_FLASH_ADDR (0x0800F800, предыдущая
// страница) — см. подробное обоснование у pool_schedule_t выше.
#define POOL_SCHEDULE_FLASH_ADDR ((uint32_t *)0x0800FC00)
